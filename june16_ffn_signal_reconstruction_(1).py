"""## Hyperparameter sweep

Grid over architecture (width/depth, Fourier frequencies) and the main training knobs (learning rate, physics gradient fraction, boundary weight). Run on GPU; trim grids or `SWEEP_EPOCHS` for a quick scan, then refit the winner at full `EPOCHS`.
"""

# ---------------------------------------------------------------------------
# Hyperparameter sweep (run on GPU)
# ---------------------------------------------------------------------------
# Grid over architecture and the main training knobs. Each configuration
# temporarily overrides the relevant globals, rebuilds the network, trains, and
# records the best held-out validation data MSE plus the final physics terms.
# Results are sorted best-first and returned as a DataFrame.
#
# This is deliberately a cell you launch on your A100: a full grid is many
# minutes of GPU time per point. Trim the grids or set SWEEP_EPOCHS lower for a
# quick scan, then refit the winner at full EPOCHS.

import itertools

SWEEP_EPOCHS = 60          # shorter than EPOCHS for scanning; refit winner after
SWEEP_SEED = SEED

SWEEP_GRID = {
    "HIDDEN_SIZES": [[128, 128, 128], [256, 256, 256], [256, 256, 256, 256]],
    "NUM_FREQ": [96, 160, 256],
    "LR": [1e-3, 2e-3],
    "BALANCE_ALPHA": [0.1, 0.3, 0.5],
    "W_BDRY": [0.5, 1.0, 2.0],
}

# Globals the sweep is allowed to mutate. Saved and restored around the loop.
_SWEEP_KEYS = list(SWEEP_GRID.keys()) + ["EPOCHS"]


def _snapshot_globals(keys):
    return {k: globals()[k] for k in keys}


def _restore_globals(snap):
    for k, v in snap.items():
        globals()[k] = v


def run_sweep(data, device, grid=SWEEP_GRID, sweep_epochs=SWEEP_EPOCHS,
              seed=SWEEP_SEED, verbose=False):
    saved = _snapshot_globals(_SWEEP_KEYS)
    names = list(grid.keys())
    combos = list(itertools.product(*[grid[n] for n in names]))
    print(f"[sweep] {len(combos)} configurations x {sweep_epochs} epochs each")

    rows = []
    try:
        globals()["EPOCHS"] = sweep_epochs
        for ci, combo in enumerate(combos, 1):
            cfg = dict(zip(names, combo))
            for k, v in cfg.items():
                globals()[k] = v
            set_seed(seed)
            net = make_net(data["scalers"], seed).to(device)
            t0 = time.time()
            hist, metrics = train(net, data, device, verbose=verbose)
            elapsed = (time.time() - t0) / 60.0
            row = dict(cfg)
            row["HIDDEN_SIZES"] = "x".join(map(str, cfg["HIDDEN_SIZES"]))
            row["val_data_mse"] = metrics["val_data_mse"]
            row["final_wave"] = hist["val_wave"][-1]
            row["final_gauge"] = hist["val_gauge"][-1]
            row["final_ic"] = hist["val_ic"][-1]
            row["final_bdry"] = hist["val_bdry"][-1]
            row["minutes"] = elapsed
            rows.append(row)
            print(f"  [{ci:02d}/{len(combos)}] {row['HIDDEN_SIZES']:>18} "
                  f"F={cfg['NUM_FREQ']:>3} lr={cfg['LR']:.0e} "
                  f"a={cfg['BALANCE_ALPHA']:.1f} wB={cfg['W_BDRY']:.1f} "
                  f"-> val {row['val_data_mse']:.3e} ({elapsed:.1f}m)")
    finally:
        _restore_globals(saved)

    results = pd.DataFrame(rows).sort_values("val_data_mse").reset_index(drop=True)
    print("\n[sweep] best configuration:")
    print(results.iloc[0].to_string())
    return results


