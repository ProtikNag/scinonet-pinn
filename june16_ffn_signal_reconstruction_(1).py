"""## Fourier-feature embedding

Dispersion-informed features: `B` is learnable but clamped each step to
per-dimension frequency bands derived from the Lamb-wave dispersion curve, and
the amplitude `A` sits outside the sinusoids. Bands are expressed directly in
standardized cycles-per-unit (a physical frequency `f` maps to `f * std`).
"""

class SpecializedFourierFeatures(nn.Module):
    def __init__(self, lo, hi, num_frequencies, seed=0):
        super().__init__()
        self.in_features = lo.numel()
        self.num_frequencies = num_frequencies
        lo = lo.to(torch.float64).unsqueeze(1)
        hi = hi.to(torch.float64).unsqueeze(1)

        gen = torch.Generator().manual_seed(seed)
        u = torch.rand(self.in_features, num_frequencies, generator=gen, dtype=torch.float64)
        self.B = nn.Parameter(lo + u * (hi - lo))
        self.A = nn.Parameter(torch.ones(num_frequencies, dtype=torch.float64))
        self.register_buffer("B_min", lo)
        self.register_buffer("B_max", hi)

    @property
    def out_features(self) -> int:
        return 2 * self.num_frequencies

    def clamp_B(self) -> None:
        with torch.no_grad():
            self.B.clamp_(self.B_min, self.B_max)

    def forward(self, x):
        proj = 2.0 * np.pi * (x @ self.B.to(x))
        amp = self.A.to(x)
        return torch.cat([amp * torch.sin(proj), amp * torch.cos(proj)], dim=-1)


def make_features(sc, seed):
    # Each spatial band is the physical wavenumber limit times that axis's own
    # length scale, since x_hat = (x - mu) / L_axis. The temporal band uses tau.
    ks = K_MAX_SPATIAL * SPATIAL_SCALE
    lo = torch.tensor([-ks * sc.Lx, -ks * sc.Ly, -ks * sc.Lz, 0.0], dtype=torch.float64)
    hi = torch.tensor([ks * sc.Lx, ks * sc.Ly, ks * sc.Lz, F_MAX_HZ * sc.tau], dtype=torch.float64)
    return SpecializedFourierFeatures(lo, hi, NUM_FREQ, seed=seed)

"""## Model

Fourier-feature MLP mapping `(x, y, z, t)` to the four potentials. The two
standardized wave speeds are stored as log-parameters but held fixed so that
`cp / cs` stays at its physical value.
"""

_ACTIVATIONS = {"gelu": nn.GELU, "tanh": nn.Tanh, "silu": nn.SiLU}


class PotentialNet(nn.Module):
    """Fourier-feature MLP mapping standardized (x, y, z, t) to four potentials.

    In the derivation's non-dimensional form the phi wave equation has unit
    leading coefficient, and the three psi equations carry gamma^2 = (c_s/c_p)^2
    on their Laplacian. Only gamma is therefore a physical parameter; it is held
    fixed at its handbook value so the P/S speed ratio is exact.
    """

    def __init__(self, features, hidden_sizes, gamma_init,
                 activation="tanh", concat_raw=True):
        super().__init__()
        self.features = features
        self.concat_raw = concat_raw
        act = _ACTIVATIONS[activation]
        in_dim = features.out_features + (features.in_features if concat_raw else 0)
        layers, prev = [], in_dim
        for width in hidden_sizes:
            layers += [nn.Linear(prev, width), act()]
            prev = width
        self.backbone = nn.Sequential(*layers)
        self.head = nn.Linear(prev, 4)   # phi, psi_x, psi_y, psi_z
        self.log_gamma = nn.Parameter(torch.tensor(float(np.log(gamma_init)),
                                                    dtype=torch.float64))

    @property
    def gamma(self):
        return torch.exp(self.log_gamma)

    def forward(self, x):
        e = self.features(x)
        if self.concat_raw:
            e = torch.cat([e, x], dim=-1)
        return self.head(self.backbone(e))


def make_net(sc, seed):
    net = PotentialNet(make_features(sc, seed), HIDDEN_SIZES,
                       gamma_init=sc.gamma,
                       activation=ACTIVATION, concat_raw=CONCAT_RAW)
    net.log_gamma.requires_grad_(False)   # fixed handbook speed ratio
    return net

"""## Physics

Displacement comes from the Helmholtz combination of potential first
derivatives, with cross-axis curl terms weighted by the aspect ratios
`beta_y, beta_z`. The wave residual follows the non-dimensional derivation
`coeff^2 (q_xx + beta_y^2 q_yy + beta_z^2 q_zz) - q_tt`, where `coeff = 1`
for `phi` (its equation fixes `tau = L_x/c_p`) and `coeff = gamma` for the
three `psi`. The Coulomb gauge enforces `div(psi) = 0`; the rest initial
condition forces the potentials and their time-derivatives to vanish at
`t = 0`. The network operates entirely on standardized quantities; physical
units are restored only at reconstruction by multiplying by `s_f`.
"""

def _grad(out, x):
    return torch.autograd.grad(out, x, grad_outputs=torch.ones_like(out),
                               create_graph=True)[0]


def displacement(model, x, beta_y, beta_z):
    """Helmholtz displacement u = grad(phi) + curl(psi) in standardized form.

    Physical derivatives map to standardized ones by d/dx = (1/Lx) d/dx_hat,
    etc. Writing every term over the common factor 1/Lx (so the leading grad
    phi terms have unit coefficient) the cross-axis curl terms pick up the
    aspect ratios beta_y = Lx/Ly and beta_z = Lx/Lz:

        u_hat = phi_x + beta_y psi_z,y - beta_z psi_y,z
        v_hat = beta_y phi_y + beta_z psi_x,z - psi_z,x
        w_hat = beta_z phi_z + psi_y,x - beta_y psi_x,y

    The overall constant scale s_f Lx is absorbed into s_f at decode time.
    """
    p = model(x)
    gphi = _grad(p[:, 0], x); gpx = _grad(p[:, 1], x)
    gpy = _grad(p[:, 2], x);  gpz = _grad(p[:, 3], x)
    u = gphi[:, 0] + beta_y * gpz[:, 1] - beta_z * gpy[:, 2]
    v = beta_y * gphi[:, 1] + beta_z * gpx[:, 2] - gpz[:, 0]
    w = beta_z * gphi[:, 2] + gpy[:, 0] - beta_y * gpx[:, 1]
    return torch.stack([u, v, w], dim=1)


def _wave_residual(q, x, coeff2, beta_y, beta_z):
    """Derivation residual: coeff2 (q_xx + beta_y^2 q_yy + beta_z^2 q_zz) - q_tt.

    coeff2 is 1 for phi (its equation set tau = Lx/c_p) and gamma^2 for the
    three psi components. A solution of the wave equation makes this vanish.
    """
    g = _grad(q, x)
    q_xx = _grad(g[:, 0], x)[:, 0]
    q_yy = _grad(g[:, 1], x)[:, 1]
    q_zz = _grad(g[:, 2], x)[:, 2]
    q_tt = _grad(g[:, 3], x)[:, 3]
    laplacian = q_xx + beta_y ** 2 * q_yy + beta_z ** 2 * q_zz
    return coeff2 * laplacian - q_tt


def physics_residuals(model, x, beta_y, beta_z):
    p = model(x)
    gamma2 = model.gamma ** 2
    one = torch.ones_like(gamma2)
    r_phi = _wave_residual(p[:, 0], x, one, beta_y, beta_z)
    r_px = _wave_residual(p[:, 1], x, gamma2, beta_y, beta_z)
    r_py = _wave_residual(p[:, 2], x, gamma2, beta_y, beta_z)
    r_pz = _wave_residual(p[:, 3], x, gamma2, beta_y, beta_z)
    # Coulomb gauge div(psi) = 0 in standardized variables, same aspect-ratio
    # weighting as the displacement curl terms.
    gauge = (_grad(p[:, 1], x)[:, 0] + beta_y * _grad(p[:, 2], x)[:, 1]
             + beta_z * _grad(p[:, 3], x)[:, 2])
    return torch.stack([r_phi, r_px, r_py, r_pz], dim=1), gauge


def ic_residual(model, x):
    p = model(x)
    p_t = torch.stack([_grad(p[:, k], x)[:, 3] for k in range(4)], dim=1)
    return torch.cat([p, p_t], dim=1)

"""## Dirichlet boundary condition

Optional fixed-value (Dirichlet) constraint on chosen plate edges. Set the edges, components, and pinned physical value in the config block. A value of 0 clamps the edge; the loss compares in standardized space.
"""

# ---------------------------------------------------------------------------
# Dirichlet boundary condition
# ---------------------------------------------------------------------------
# The manuscript models a finite plate; you can optionally pin the displacement
# on chosen physical edges to a fixed value. Set BDRY_ENABLE = True and fill in
# the edges and value below. Edges are named by the axis and side in physical
# coordinates: "x_min", "x_max", "y_min", "y_max", "z_min", "z_max".
#
#   BDRY_VALUE is the PHYSICAL displacement (mm) the edge is pinned to, applied
#   to whichever components BDRY_COMPONENTS lists. It is divided by s_f inside
#   the loss so the comparison happens in standardized space. A pinned value of
#   0.0 is a clamped (fixed, non-moving) edge.

BDRY_ENABLE = True
BDRY_EDGES = ["x_min", "x_max", "y_min", "y_max"]  # perimeter of the plate
BDRY_COMPONENTS = ["u", "v", "w"]                  # which displacement comps to pin
BDRY_VALUE = 0.0                                   # physical displacement [mm]
W_BDRY = 1.0                                       # boundary sub-weight
N_BDRY = 1024                                      # boundary points per step


_EDGE_AXIS = {"x_min": (0, "lo"), "x_max": (0, "hi"),
              "y_min": (1, "lo"), "y_max": (1, "hi"),
              "z_min": (2, "lo"), "z_max": (2, "hi")}
_COMP_IDX = {"u": 0, "v": 1, "w": 2}


def sample_boundary(data, n, device, dtype, edges=BDRY_EDGES):
    """Sample standardized collocation points lying on the chosen domain edges.

    For each requested edge the corresponding standardized coordinate is fixed
    to the domain min or max while the others are sampled uniformly. Points are
    split evenly across the listed edges.
    """
    lo, hi = data["colloc_lo"], data["colloc_hi"]
    lo = lo.to(device, dtype); hi = hi.to(device, dtype)
    if not edges:
        return torch.empty(0, 4, device=device, dtype=dtype, requires_grad=True)
    per = max(1, n // len(edges))
    chunks = []
    for edge in edges:
        axis, side = _EDGE_AXIS[edge]
        u = torch.rand(per, 4, device=device, dtype=dtype)
        x = lo + u * (hi - lo)
        x[:, axis] = lo[axis] if side == "lo" else hi[axis]
        chunks.append(x)
    xb = torch.cat(chunks, dim=0)
    return xb.requires_grad_(True)


def boundary_residual(model, x, sc, components=BDRY_COMPONENTS, value=BDRY_VALUE):
    """Dirichlet residual: predicted standardized displacement minus pinned value.

    The physical pinned value is converted to standardized units by dividing by
    the field scale s_f, matching the space the network predicts in.
    """
    if x.shape[0] == 0:
        return torch.zeros(0, len(components), device=x.device, dtype=x.dtype)
    disp = displacement(model, x, sc.beta_y, sc.beta_z)
    idx = [_COMP_IDX[c] for c in components]
    target_hat = value / sc.s_f
    return disp[:, idx] - target_hat

def sample_colloc(lo, hi, n, device, dtype):
    u = torch.rand(n, lo.numel(), device=device, dtype=dtype)
    return (lo.to(device, dtype) + u * (hi - lo).to(device, dtype)).requires_grad_(True)


def sample_ic(data, n, device, dtype):
    lo, hi = data["colloc_lo"], data["colloc_hi"]
    u = torch.rand(n, 4, device=device, dtype=dtype)
    x = lo.to(device, dtype) + u * (hi - lo).to(device, dtype)
    x[:, 3] = data["t0_std"]
    return x.requires_grad_(True)

"""## Training

Data loss (through the Helmholtz displacement) plus wave, gauge, and IC
residuals. The physics weight is set each epoch so the physics gradient is a
fixed fraction `alpha` of the data gradient, after a short data-only warmup. The
best model is the one with lowest validation data loss.
"""

def train(model, data, device, verbose=True):
    from torch.utils.data import DataLoader, TensorDataset

    dtype = next(model.parameters()).dtype
    model = model.to(device)
    sc = data["scalers"]
    beta_y, beta_z = sc.beta_y, sc.beta_z
    loader = DataLoader(TensorDataset(data["Xtr"], data["Ytr"]),
                        batch_size=BATCH_SIZE, shuffle=True)

    n_val = min(len(data["Xte"]), 16384)
    vperm = torch.randperm(len(data["Xte"]))[:n_val]
    Xte = data["Xte"][vperm].to(device, dtype)
    Yte = data["Yte"][vperm].to(device, dtype)

    opt = torch.optim.Adam(model.parameters(), lr=LR)
    sch = torch.optim.lr_scheduler.ReduceLROnPlateau(
        opt, mode="min", factor=SCHED_FACTOR, patience=SCHED_PATIENCE, min_lr=MIN_LR)
    mse = nn.MSELoss()
    params = [p for p in model.parameters() if p.requires_grad]

    def gnorm(loss):
        g = torch.autograd.grad(loss, params, retain_graph=True, allow_unused=True)
        return torch.sqrt(sum((gi.detach() ** 2).sum() for gi in g if gi is not None))

    # Fixed monitoring sets so physics curves are continuous from epoch 1.
    n_mon = min(N_COLLOC, 1024)
    mon_c = sample_colloc(data["colloc_lo"], data["colloc_hi"], n_mon, device, dtype)
    mon_i = sample_ic(data, min(N_IC, 512), device, dtype)
    val_c = sample_colloc(data["colloc_lo"], data["colloc_hi"], n_mon, device, dtype)
    val_i = sample_ic(data, min(N_IC, 512), device, dtype)
    mon_b = sample_boundary(data, min(N_BDRY, 512), device, dtype) if BDRY_ENABLE else None
    val_b = sample_boundary(data, min(N_BDRY, 512), device, dtype) if BDRY_ENABLE else None

    def phys_terms(xc, xi, xb):
        wave, gauge = physics_residuals(model, xc, beta_y, beta_z)
        w_l = mse(wave, torch.zeros_like(wave)).item()
        g_l = mse(gauge, torch.zeros_like(gauge)).item()
        i_l = mse(ic_residual(model, xi),
                  torch.zeros(xi.shape[0], 8, device=device, dtype=dtype)).item()
        if BDRY_ENABLE and xb is not None and xb.shape[0] > 0:
            br = boundary_residual(model, xb, sc)
            b_l = mse(br, torch.zeros_like(br)).item()
        else:
            b_l = 0.0
        return w_l, g_l, i_l, b_l

    keys = ["train_data", "train_wave", "train_gauge", "train_ic", "train_bdry",
            "train_total", "val_data", "val_wave", "val_gauge", "val_ic",
            "val_bdry", "val_total", "phys_w", "gamma"]
    hist = {k: [] for k in keys}
    best = float("inf"); best_state = copy.deepcopy(model.state_dict()); bad = 0
    phys_w = 0.0
    start = time.time()
    if verbose:
        print(f"[train] device={device} beta_y={beta_y:.3f} beta_z={beta_z:.3f} "
              f"gamma={sc.gamma:.3f} bdry={'on' if BDRY_ENABLE else 'off'} "
              f"params={sum(p.numel() for p in model.parameters()):,}")

    for ep in range(1, EPOCHS + 1):
        physics_on = ep > DATA_ONLY_EPOCHS
        model.train()
        dsum = wsum = gsum = isum = bsum = 0.0; nb = 0
        for xb_data, yb in loader:
            xb_data = xb_data.to(device, dtype).requires_grad_(True)
            yb = yb.to(device, dtype)
            opt.zero_grad()
            data_loss = mse(displacement(model, xb_data, beta_y, beta_z), yb)
            if physics_on:
                xc = sample_colloc(data["colloc_lo"], data["colloc_hi"], N_COLLOC, device, dtype)
                wave, gauge = physics_residuals(model, xc, beta_y, beta_z)
                wave_loss = mse(wave, torch.zeros_like(wave))
                gauge_loss = mse(gauge, torch.zeros_like(gauge))
                xi = sample_ic(data, N_IC, device, dtype)
                ic_loss = mse(ic_residual(model, xi),
                              torch.zeros(N_IC, 8, device=device, dtype=dtype))
                if BDRY_ENABLE:
                    xbd = sample_boundary(data, N_BDRY, device, dtype)
                    br = boundary_residual(model, xbd, sc)
                    bdry_loss = mse(br, torch.zeros_like(br))
                else:
                    bdry_loss = torch.zeros((), device=device, dtype=dtype)
                phys = (wave_loss + W_GAUGE * gauge_loss + W_IC * ic_loss
                        + W_BDRY * bdry_loss)
                if nb == 0:
                    gd, gp = gnorm(data_loss), gnorm(phys)
                    tgt = BALANCE_ALPHA * float(gd / (gp + 1e-12))
                    phys_w = (BALANCE_EMA * phys_w + (1 - BALANCE_EMA) * tgt) if phys_w > 0 else tgt
                loss = data_loss + phys_w * phys
            else:
                wave_loss = gauge_loss = ic_loss = bdry_loss = torch.zeros((), device=device, dtype=dtype)
                loss = data_loss
            loss.backward()
            if GRAD_CLIP:
                nn.utils.clip_grad_norm_(model.parameters(), GRAD_CLIP)
            opt.step()
            if hasattr(model.features, "clamp_B"):
                model.features.clamp_B()
            dsum += data_loss.item(); wsum += float(wave_loss.item())
            gsum += float(gauge_loss.item()); isum += float(ic_loss.item())
            bsum += float(bdry_loss.item()); nb += 1

        model.eval()
        Xv = Xte.clone().requires_grad_(True)
        val_data = mse(displacement(model, Xv, beta_y, beta_z), Yte).item()
        sch.step(val_data)
        tw, tg, ti, tb = phys_terms(mon_c, mon_i, mon_b)
        vw, vg, vi, vb = phys_terms(val_c, val_i, val_b)
        tphys = tw + W_GAUGE * tg + W_IC * ti + W_BDRY * tb
        vphys = vw + W_GAUGE * vg + W_IC * vi + W_BDRY * vb
        for k, val in [("train_data", dsum / nb), ("train_wave", tw), ("train_gauge", tg),
                       ("train_ic", ti), ("train_bdry", tb),
                       ("train_total", dsum / nb + phys_w * tphys),
                       ("val_data", val_data), ("val_wave", vw), ("val_gauge", vg),
                       ("val_ic", vi), ("val_bdry", vb),
                       ("val_total", val_data + phys_w * vphys),
                       ("phys_w", phys_w), ("gamma", model.gamma.item())]:
            hist[k].append(val)

        if val_data < best - 1e-12:
            best = val_data; best_state = copy.deepcopy(model.state_dict()); bad = 0
        else:
            bad += 1
        if verbose and (ep % LOG_EVERY == 0 or ep == 1):
            print(f"ep {ep:04d} | data {dsum/nb:.2e} | wave {wsum/nb:.2e} "
                  f"| gauge {gsum/nb:.2e} | ic {isum/nb:.2e} | bdry {bsum/nb:.2e} "
                  f"| val {val_data:.2e} | best {best:.2e} | w {phys_w:.3f}")
        if bad >= EARLY_STOP_PATIENCE:
            if verbose:
                print(f"early stop {ep}")
            break

    model.load_state_dict(best_state)
    if verbose:
        print(f"[train] done {(time.time()-start)/60:.2f} min | best val {best:.2e}")
    return hist, {"val_data_mse": best}

