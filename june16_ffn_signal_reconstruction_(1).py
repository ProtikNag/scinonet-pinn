"""### Standardization scalers

Each spatial axis is referred to its own length scale `L_x, L_y, L_z` (the
per-axis coordinate std), so the standardized coordinates run over O(1)
ranges. The time scale is the P-wave transit time `tau = L_x / c_p`, which
normalizes the `phi` wave equation. Two geometric aspect ratios survive,
`beta_y = L_x/L_y` and `beta_z = L_x/L_z`, together with the single material
speed ratio `gamma = c_s/c_p`. Field scale `s_f` is the common std of
`(u, v, w)`; the inverse map back to physical displacement is `u = s_f * u_hat`.
"""

class PotentialScalers:
    """Non-dimensionalization following the elastic-wave derivation.

    Each spatial axis is referred to its own physical length scale, so that the
    standardized coordinates run over O(1) ranges:

        x_hat = (x - mu_x) / L_x,   y_hat = (y - mu_y) / L_y,
        z_hat = (z - mu_z) / L_z,   t_hat = (t - mu_t) / tau.

    The time scale is fixed to the P-wave transit time across the x-extent,
    tau = L_x / c_p, which normalizes the phi wave equation. Two geometric
    aspect ratios then survive,

        beta_y = L_x / L_y,   beta_z = L_x / L_z,

    together with the single material speed ratio gamma = c_s / c_p (the only
    speed left after tau absorbs c_p). The fields are standardized by a single
    common scale s_f so the data loss compares O(1) quantities, and the inverse
    map back to physical displacement is u = s_f * u_hat.
    """

    def __init__(self, mu_x, mu_y, Lx, mu_y_, Ly, mu_z, Lz, mu_t, tau, s_f,
                 cp_mm_per_s, cs_mm_per_s):
        self.mu_x = mu_x; self.Lx = Lx
        self.mu_y = mu_y; self.Ly = Ly
        self.mu_z = mu_z; self.Lz = Lz
        self.mu_t = mu_t; self.tau = tau
        self.s_f = s_f
        self.cp = cp_mm_per_s; self.cs = cs_mm_per_s

    @classmethod
    def fit(cls, frame: "pd.DataFrame", cp_mm_per_s: float,
            cs_mm_per_s: float) -> "PotentialScalers":
        Lx = float(frame["x"].std() + 1e-12)
        Ly = float(frame["y"].std() + 1e-12)
        Lz = float(frame["z"].std() + 1e-12)
        # Guard a degenerate through-thickness spread (e.g. very few z layers):
        # fall back to the in-plane scale so beta_z stays finite and O(1).
        if Lz < 1e-9:
            Lz = Lx
        tau = Lx / cp_mm_per_s
        uvw = np.concatenate([frame["u"].to_numpy(), frame["v"].to_numpy(),
                              frame["w"].to_numpy()])
        s_f = float(uvw.std() + 1e-30)
        return cls(
            mu_x=float(frame["x"].mean()), Lx=Lx,
            mu_y=float(frame["y"].mean()), mu_y_=None, Ly=Ly,
            mu_z=float(frame["z"].mean()), Lz=Lz,
            mu_t=float(frame["t"].mean()), tau=tau,
            s_f=s_f, cp_mm_per_s=cp_mm_per_s, cs_mm_per_s=cs_mm_per_s,
        )

    @property
    def beta_y(self) -> float:
        """Aspect ratio L_x / L_y multiplying the y second derivative."""
        return self.Lx / self.Ly

    @property
    def beta_z(self) -> float:
        """Aspect ratio L_x / L_z multiplying the z second derivative."""
        return self.Lx / self.Lz

    @property
    def gamma(self) -> float:
        """Material speed ratio c_s / c_p (the surviving speed parameter)."""
        return self.cs / self.cp

    def encode(self, x, y, z, t) -> np.ndarray:
        return np.stack([(x - self.mu_x) / self.Lx,
                         (y - self.mu_y) / self.Ly,
                         (z - self.mu_z) / self.Lz,
                         (t - self.mu_t) / self.tau], axis=1)

    def decode_fields(self, uvw_hat: np.ndarray) -> np.ndarray:
        """Map standardized displacement back to physical units (mm)."""
        return uvw_hat * self.s_f

"""### Train/test split (spatial then temporal)

Stage 1 removes `N_HELD_SPATIAL` unique points from training entirely (spatial
holdout, spread across neighborhoods). Stage 2 keeps a random fraction
`SUBSAMPLE_KEEP` of timesteps on every remaining point. Scalers are fitted on
training rows only.
"""

def _choose_spatial_holdout(xy_points, nbhd_of_point, n_held, seed):
    """Pick n_held unique (x, y) indices to exclude, spread across groups.

    Points are drawn round-robin over neighborhoods so the spatial holdout is
    not clustered in a single region. Within each group the choice is random
    under the given seed. Returns a sorted list of indices into xy_points.
    """
    rng = np.random.RandomState(seed)
    groups = {}
    for idx, g in enumerate(nbhd_of_point):
        groups.setdefault(g, []).append(idx)
    for g in groups:
        rng.shuffle(groups[g])
    order = sorted(groups.keys(), key=lambda g: str(g))
    held, gi = [], 0
    n_held = min(n_held, len(xy_points))
    while len(held) < n_held:
        g = order[gi % len(order)]
        if groups[g]:
            held.append(groups[g].pop())
        gi += 1
        if gi > len(order) * (len(xy_points) + 1):  # safety: pool exhausted
            break
    return sorted(held)


def build_dataset(df: pd.DataFrame, subsample_keep: float, n_held_spatial: int,
                  seed: int):
    """Two-stage split: spatial holdout, then temporal subsample on the rest.

    is_train     True for the kept training rows (trained points, kept timesteps)
    held_spatial True for every row of a spatially excluded point (never trained)
    """
    df = df.copy()

    # --- unique spatial points and their neighborhood ids -------------------
    xy_df = df[["x", "y", NBHD_COL]].drop_duplicates(subset=["x", "y"])
    xy_df = xy_df.sort_values(["x", "y"]).reset_index(drop=True)
    xy_points = xy_df[["x", "y"]].to_numpy()
    nbhd_of_point = xy_df[NBHD_COL].to_numpy()
    z_values = np.sort(df["z"].unique())

    # --- stage 1: spatial holdout (exclude whole signals) -------------------
    held_idx = _choose_spatial_holdout(xy_points, nbhd_of_point,
                                       n_held_spatial, seed)
    held_xy = set(map(tuple, xy_points[held_idx]))
    point_key = list(zip(df["x"].to_numpy(), df["y"].to_numpy()))
    held_spatial = np.array([k in held_xy for k in point_key])
    df["held_spatial"] = held_spatial

    # --- stage 2: temporal subsample on the trained points only -------------
    rng = np.random.RandomState(seed + 1)
    keep = (rng.rand(len(df)) < subsample_keep) & (~held_spatial)
    df["is_train"] = keep

    tr = df[keep]
    # Validation = temporal holdout on the trained points (seen spatial points,
    # unseen timesteps). The spatially held points are evaluated separately.
    te = df[(~keep) & (~held_spatial)]
    sc = PotentialScalers.fit(tr, CP_MM_PER_S, CS_MM_PER_S)

    def to_xy(frame):
        X = sc.encode(frame["x"].to_numpy(), frame["y"].to_numpy(),
                      frame["z"].to_numpy(), frame["t"].to_numpy())
        Y = frame[FIELD_COLS].to_numpy() / sc.s_f
        return (torch.tensor(X, dtype=torch.float64),
                torch.tensor(Y, dtype=torch.float64))

    Xtr, Ytr = to_xy(tr)
    Xte, Yte = to_xy(te)

    allc = sc.encode(df["x"].to_numpy(), df["y"].to_numpy(),
                     df["z"].to_numpy(), df["t"].to_numpy())
    colloc_lo = torch.tensor(allc.min(0), dtype=torch.float64)
    colloc_hi = torch.tensor(allc.max(0), dtype=torch.float64)
    t0_std = float((0.0 - sc.mu_t) / sc.tau)

    return {
        "Xtr": Xtr, "Ytr": Ytr, "Xte": Xte, "Yte": Yte, "scalers": sc,
        "xy_points": xy_points, "nbhd_of_point": nbhd_of_point,
        "held_spatial_idx": held_idx, "z_values": z_values, "df": df,
        "colloc_lo": colloc_lo, "colloc_hi": colloc_hi, "t0_std": t0_std,
    }

