# NBHD_COL stays None and the whole sampled region is treated as one group.
_NBHD_CANDIDATES = ["nbhd", "neighborhood", "neighbourhood", "neighbor",
                    "neighbour", "nbr", "group", "region", "cluster", "patch",
                    "block", "zone"]

NBHD_COL = None  # resolved at load time; printed so it can be verified


def _detect_nbhd_col(columns) -> "str | None":
    """Return the neighborhood id column name, or None if absent.

    Coordinate and field columns are never treated as neighborhood ids even if
    a candidate name happens to collide.
    """
    reserved = {"x", "y", "z", "t", "u", "v", "w", "t_idx"}
    lower = {c.lower(): c for c in columns}
    for cand in _NBHD_CANDIDATES:
        if cand in lower and lower[cand].lower() not in reserved:
            return lower[cand]
    # Heuristic fallback: any column whose name contains "nbhd"/"neigh".
    for c in columns:
        cl = c.lower()
        if ("nbhd" in cl or "neigh" in cl) and cl not in reserved:
            return c
    return None


def load_full_signal(csv_path: str) -> pd.DataFrame:
    global NBHD_COL
    df = pd.read_csv(csv_path)
    NBHD_COL = _detect_nbhd_col(df.columns)
