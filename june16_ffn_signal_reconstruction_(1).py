def select_seen_points(data, k=5, seed=SEED):
    """Return up to k indices of trained (seen) points, spread across groups.

    A seen point has at least one kept training timestep and is not in the
    spatial holdout. Indices are into data["xy_points"].
    """
    df = data["df"]
    xy = data["xy_points"]
    held = set(data["held_spatial_idx"])
    key_to_idx = {(px, py): i for i, (px, py) in enumerate(map(tuple, xy))}
    trained_keys = (df[df["is_train"]][["x", "y"]]
                    .drop_duplicates().itertuples(index=False, name=None))
    seen = sorted({key_to_idx[k] for k in trained_keys
                   if k in key_to_idx and key_to_idx[k] not in held})
    if len(seen) <= k:
        return seen
    nbhd = data["nbhd_of_point"]
    rng = np.random.RandomState(seed)
    by_group = {}
    for i in seen:
        by_group.setdefault(nbhd[i], []).append(i)
    for g in by_group:
        rng.shuffle(by_group[g])
    order = sorted(by_group.keys(), key=lambda g: str(g))
    chosen, gi = [], 0
    while len(chosen) < k:
        g = order[gi % len(order)]
        if by_group[g]:
            chosen.append(by_group[g].pop())
        gi += 1
    return sorted(chosen)


