"""## Held-out spatial point prediction

Time-signal prediction at the spatially excluded points. These points were
removed from training entirely, so the model has seen no timestep of them at
all. The whole curve is a spatial extrapolation, not a fit.
"""

def select_heldout_points(data, k=5, seed=SEED):
    """Return up to k indices of spatially held-out points.

    These are the points excluded from training entirely in stage 1 of the
    split. If more than k exist, a seeded random subset spread across their
    neighborhoods is returned. Indices are into data["xy_points"].
    """
    held = list(data["held_spatial_idx"])
    if len(held) <= k:
        return sorted(held)
    nbhd = data["nbhd_of_point"]
    rng = np.random.RandomState(seed)
    # round-robin over neighborhoods so the chosen subset stays spread out
    by_group = {}
    for i in held:
        by_group.setdefault(nbhd[i], []).append(i)
    for g in by_group:
        rng.shuffle(by_group[g])
    order = sorted(by_group.keys(), key=lambda g: str(g))
    chosen, gi = [], 0
    while len(chosen) < k:
        g = order[gi % len(order)]
        if by_group[g]:
            chosen.append(by_group[g].pop())
        gi += 1
    return sorted(chosen)


